                            <li>* Hire the agency who can transmit live stream. </li>
                            <li>* Using VC system (Min 2 Mbps or 4 Mbps capacity): Video conferencing system is required to take live A/V feed from Camera and PA system to transmit it from Venue to Webcast control room at NIC Hq. The IP is to be configured on VC system and using one side transmission, NIC will receive live feed and further live webcast will be provided.</li>
                        </ul>
                    </li>
                </ul>

