                <h4>Case-1: Live telecast available on DD channel</h4>
                <p>NIC has setup of DD Direct+ systems at webcast control room at NIC Hq to receive all national and regional channels of Doordarshan. On receiving the live telecast over DD channel, we will provide live webcast from webcast control room.</p>
                <h4>Case-2: Live Telecast on DD channel NOT available</h4>
                <ul>
                    <li>
                        <p>Network Requirement –If live telecast is not available on any DD channel then Network Connectivity between Venue and NIC Hq is required. The connectivity can be any of the following –</p>
                        <ul>
                            <li>* NICNET or NKN connectivity at venue</li>
                            <li>* Dedicated leased line of Minimum 2 Mbps for each live stream (in case multiple live streams are required i.e. from more than one hall parallel)
                            </li>
                            <li>* Broadband connectivity with one Public IP (to have IP to IP transmission for security reason)</li>
                        </ul>
                        <p><strong>Note: The network connectivity has to be arranged by the applicant.</strong></p>
                    </li>
                    <li>Availability of Public Address (PA) system and video cameras. This setup will be required to capture the live event for further live webcast. If these setup are not available then the applicant may hire the agency for the same.</li>
